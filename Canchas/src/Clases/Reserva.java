    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
                                                  
        if (jTextField1.getText().isEmpty()|| jTextField2.getText().isEmpty() || jTextField3.getText().isEmpty() || jTextField4.getText().isEmpty() || jTextField5.getText().isEmpty() ){
            JOptionPane.showMessageDialog(this, "Datos Incorrectos", "Informacion", JOptionPane.INFORMATION_MESSAGE);
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            jTextField1.requestFocus();
            jTextField2.requestFocus(); 
            jTextField3.requestFocus();
            jTextField4.requestFocus();
            jTextField5.requestFocus();
        } else {
            try{
                res=Conexiones.Conexion.Consulta("Select COUNT(ID_CLIENTE) from ARRIENDA where ID_CLIENTE='"+jTextField1.getText()+ "'");
                try{
                    while(res.next()){
                        cont= res.getInt(1);
                    }
        
                }catch (SQLException e){
                    
                }
                if (cont>=1){
                    JOptionPane.showMessageDialog(this, "El elemento ya existe", "Informacion", JOptionPane.INFORMATION_MESSAGE);
                } else{
                       // String aux=jTextField2.getText();
                        //String aux=jDateChooser1.getDateFormatString();
                        Procedimientos.GenerarReserva(jTextField5.getText() , jTextField1.getText(),jTextField3.getText(), jTextField4.getText(),jTextField2.getText());
                        
                        jTextField1.setText("");
                        jTextField2.setText("");
                        jTextField3.setText("");
                        jTextField4.setText("");
                        jTextField5.setText("");
                        jTextField1.requestFocus();
                        jTextField2.requestFocus();
                        jTextField3.requestFocus();
                        jTextField4.requestFocus();
                        jTextField5.requestFocus();
                        
                        JOptionPane.showMessageDialog(this, "Datos han sido guardados");
                    
                    
                }
            }catch (SQLException e){
                
            }
        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

