    public void CargarReserva()
    {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0);
        res= Conexiones.Conexion.Consulta("select ID_CLIENTE,N_DE_CANCHA,HORARIO from  ARRIENDA");
        try{
            while(res.next()){
                Vector v = new Vector();
                v.add(res.getInt(1));
                v.add(res.getInt(2));
                v.add(res.getString(3));
                
                
                
            
                modelo.addRow(v);
                jTable1.setModel(modelo);
            }
        }catch (SQLException e){
        }
    
    }

